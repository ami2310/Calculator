package com.JavaProg.Calculator;

public class Calculator 
{
	public int Addition(int a, int b)
	{
		int c=a+b;
		return c;
	}
	public int Substraction(int n1,int n2)
	{
		int c=n1-n2;
		return c;
	}
	
	public int Multiplication(int n1, int n2)
	{
		int c=n1*n2;
		return c;
	}
	
	public int Division(int n1,int n2)
	{
		int c=n1/n2;
		return c;
	}
}
